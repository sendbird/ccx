func TestRender_TextOnly(t *testing.T) {
	entry := session.Entry{
		Role:      "user",
		Timestamp: time.Date(2025, 6, 1, 12, 0, 0, 0, time.UTC),
		Content: []session.ContentBlock{
			{Type: "text", Text: "Hello, can you help me with this?"},
		},
	}
	folds := defaultFolds(entry)
	rp := renderFullMessageWithCursor(entry, 80, folds, nil, -1)
	assertGolden(t, "text_only", stripANSIForGolden(rp.content))
}

