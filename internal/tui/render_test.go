func TestRender_BlockStarts(t *testing.T) {
	entry := session.Entry{
		Role:      "assistant",
		Timestamp: time.Date(2025, 6, 1, 12, 0, 0, 0, time.UTC),
		Content: []session.ContentBlock{
			{Type: "text", Text: "First block"},
			{Type: "system_tag", TagName: "task-notification", Text: "task reminder"},
			{Type: "text", Text: "Second block"},
			{Type: "tool_use", ToolName: "Bash", ToolInput: `{"command":"ls"}`},
		},
	}
	folds := defaultFolds(entry)
	rp := renderFullMessageWithCursor(entry, 80, folds, nil, -1)

	// blockStarts should have one entry per content block
	if len(rp.blockStarts) != len(entry.Content) {
		t.Errorf("blockStarts length = %d, want %d", len(rp.blockStarts), len(entry.Content))
	}

	// Each subsequent block start should be >= previous
	for i := 1; i < len(rp.blockStarts); i++ {
		if rp.blockStarts[i] < rp.blockStarts[i-1] {
			t.Errorf("blockStarts[%d]=%d < blockStarts[%d]=%d",
				i, rp.blockStarts[i], i-1, rp.blockStarts[i-1])
		}
	}
}

func TestRender_CompactMessage(t *testing.T) {
	entry := session.Entry{
		Role:      "user",
		Timestamp: time.Date(2025, 6, 1, 12, 30, 0, 0, time.UTC),
		Content: []session.ContentBlock{
			{Type: "system_tag", TagName: "system-reminder", Text: "You must follow these rules"},
			{Type: "text", Text: "What is the weather?"},
		},
	}
	got := renderCompactMessage(entry, 80, 2)
	plain := stripANSI(got)

	// Compact message should show the text content, not the system_tag
	if strings.Contains(plain, "system-reminder") {
		t.Error("compact message should not show system-reminder tag name")
	}
	if !strings.Contains(plain, "What is the weather?") && !strings.Contains(plain, "weather") {
		t.Error("compact message should contain the actual text preview")
	}
}
