// updateGolden returns true if golden files should be regenerated.
func updateGolden() bool {
	return os.Getenv("UPDATE_GOLDEN") != ""
}

// goldenPath returns the path to a golden file.
func goldenPath(name string) string {
	return filepath.Join("testdata", name+".golden")
}

// assertGolden compares rendered output against a golden file.
// Run with UPDATE_GOLDEN=1 to regenerate.
func assertGolden(t *testing.T, name, got string) {
	t.Helper()
	path := goldenPath(name)

	if updateGolden() {
		if err := os.MkdirAll("testdata", 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(path, []byte(got), 0o644); err != nil {
			t.Fatal(err)
		}
		t.Logf("updated golden file: %s", path)
		return
	}

	want, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("missing golden file %s (run with UPDATE_GOLDEN=1): %v", path, err)
	}
	if got != string(want) {
		// Show diff context
		gotLines := strings.Split(got, "\n")
		wantLines := strings.Split(string(want), "\n")
		maxLines := max(len(gotLines), len(wantLines))
		for i := range min(maxLines, 50) {
			g, w := "", ""
			if i < len(gotLines) {
				g = gotLines[i]
			}
			if i < len(wantLines) {
				w = wantLines[i]
			}
			if g != w {
				t.Errorf("line %d diff:\n  got:  %q\n  want: %q", i+1, g, w)
			}
		}
		if maxLines > 50 {
			t.Errorf("... and %d more lines", maxLines-50)
		}
	}
}

// stripANSIForGolden removes ANSI escape sequences so golden files are readable.
func stripANSIForGolden(s string) string {
	return stripANSI(s)
}

